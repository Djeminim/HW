#include <stdio.h>
#include <stdint.h>

int main()
{
    uint32_t n;
    int k;

    // Ввод данных
    scanf("%u", &n);  // Используем %u для чтения беззнакового числа

    scanf("%d", &k);

    // Проверка корректности ввода K (хотя это уже гарантировано условием)
    if (k < 1 || k > 31)
    {
        printf("Ошибка: K должно быть в диапазоне от 1 до 31.\n");
        return 1; // Возвращаем код ошибки
    }

    // Выделение K младших битов
    uint32_t mask = (1U << k) - 1; // Создаем маску из K единичных битов
    uint32_t result = n & mask;    // Применяем побитовое И к N и маске

    // Вывод результата
    printf("%u\n", result); // Используем %u для вывода беззнакового числа

    return 0;
}